    private void jBtnPokreniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnPokreniActionPerformed
        if (serverskaNit == null || !serverskaNit.isAlive()) {
            try {
                serverskaNit = new ServerskaNit();
                serverskaNit.start();

                jBtnPokreni.setEnabled(false);
                jBtnZaustavi.setEnabled(true);
                jBtnProveri.setEnabled(false);
                
                jLblStatus.setText("Server je pokrenut");

            } catch (IOException ex) {
                System.out.println(ex.getMessage());
            }
        } else {
            System.err.println("Serverska nit je vec pokrenuta");
        }
    }//GEN-LAST:event_jBtnPokreniActionPerformed

    private void jBtnZaustaviActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnZaustaviActionPerformed
        try {
            if (serverskaNit.getServerSocket() != null && serverskaNit.getServerSocket().isBound()) {
                serverskaNit.getServerSocket().close();
                
                jBtnPokreni.setEnabled(true);
                jBtnZaustavi.setEnabled(false);
                jBtnProveri.setEnabled(true);
                
                jLblStatus.setText("Server je zaustavljen");
            }
        } catch (IOException ex) {
            System.err.println("Serverska nit je vec prekinuta");
        }
    }//GEN-LAST:event_jBtnZaustaviActionPerformed

