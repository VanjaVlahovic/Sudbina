    private void jBtnPotvrdiActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnPotvrdiActionPerformed
        try {
            String url = jTxtUrl.getText();
            String user = jTxtUsername.getText();
            String password = jTxtPassword.getText();
            String port = jTxtPort.getText();
            
            Properties properties = new Properties();
            OutputStream output = new FileOutputStream("config/dbconfig.properties");
            properties.setProperty("url", url);
            properties.setProperty("username", user);
            properties.setProperty("password", password);
            properties.setProperty("port", port);
            properties.store(output, null);
            JOptionPane.showMessageDialog(this, "Sacuvani parametri baze.");
            pokreni.setEnabled(true);
            dispose();
        } catch (IOException ex) {
            JOptionPane.showMessageDialog(this, "Greska pri cuvanju parametara");
        }
        
    }//GEN-LAST:event_jBtnPotvrdiActionPerformed


