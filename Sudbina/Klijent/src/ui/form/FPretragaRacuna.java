    private void jBtnIzmeniActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnIzmeniActionPerformed
        int red = jTable1.getSelectedRow();
        if (red != -1) {
            Racun r = model.getRacun(red);
            if (r.isStorniran()){
                JOptionPane.showMessageDialog(this, "Ovaj račun je storniran i ne moze se menjati!");
                return;
            }
            new FRacun(r).setVisible(true);
        } else {
            JOptionPane.showMessageDialog(this, "Niste selektovali odgovarajuci račun!");
        }
    }//GEN-LAST:event_jBtnIzmeniActionPerformed

    private void jBtnStornirajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnStornirajActionPerformed
        int red = jTable1.getSelectedRow();
        if (red != -1) {
            Racun original = model.getRacun(red);
            if(original.isStorniran()){
                 JOptionPane.showMessageDialog(this, "Izabran račun je već storniran!");
                 return;
            }
            try {
                Racun storniran = new Racun(original.getSifra(), original.getVreme(), original.getKonobar(), original.getUkupnaVrednost(), true);
                Controller.getInstance().stornirajRacun(storniran);
                JOptionPane.showMessageDialog(this, "Sistem je stornirao račun!");
            } catch (Exception ex) {
                JOptionPane.showMessageDialog(this, ex.getMessage());
            }
        } else {
            JOptionPane.showMessageDialog(this, "Niste selektovali odgovarajuci račun!");
        }
    }//GEN-LAST:event_jBtnStornirajActionPerformed

