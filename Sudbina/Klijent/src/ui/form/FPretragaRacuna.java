    private void popuniFormu() {
        List<Racun> racuni;
        try {
            racuni = Controller.getInstance().vratiRacune(new Racun());
            model = new RacuniTableModel(racuni);
            jTable1.setModel(model);
            nit = new nitRacun(model, jCheckBoxKonobar, jCheckBoxDatum, jTxtKonobar, jTxtDatum, this);
            nit.start();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }

    }
