    private void popuniFormu() {
        model = new StoTableModel();
        jTable1.setModel(model);
        jTxtSifra.setText(sifraRezervacije + "");
        jCBStolovi.removeAllItems();
    }
    
    private void osveziUkupanBrojObezbedjenihMesta() {
        int obezbedjenaMesta = 0;
        List<Sto> stolovi = model.getStolovi();
        for (Sto s : stolovi) {
            obezbedjenaMesta = obezbedjenaMesta + s.getBrojMesta();
        }
        jTxtUkupno.setText(obezbedjenaMesta + "");
    }
    
    private Rezervacija validacija() {
        String naIme = jTxtNaIme.getText();
        String vreme = jTxtVreme.getText();
        String brojMesta = jTxtBrojMesta.getText();
        String Napomena = jTxtAreaNapomena.getText();
        String UkupnoMesta = jTxtUkupno.getText();
        
        if (naIme.isEmpty() || vreme.isEmpty() || brojMesta.isEmpty()) {
            throw new RuntimeException("Sva polja osim napomene moraju biti popunjena!");
        }
        Date vremeD = new Date();
        try {
            vremeD = new Date(sdf.parse(vreme).getTime());
        } catch (ParseException e) {
            throw new RuntimeException("Datum nije pravilno unet");
        }
        int brojMestaI = 0;
        try {
            brojMestaI = Integer.valueOf(brojMesta);
            if (brojMestaI <= 0) {
                throw new RuntimeException("Broj mesta mora biti pozitivan broj veci od nule!");
            }
        } catch (NumberFormatException e) {
            throw new RuntimeException("Unesite pravilno broj mesta (u formi broja)");
        }
        
        List<Sto> stolovi = model.getStolovi();
        
        if (stolovi.isEmpty()) {
            throw new RuntimeException("Morate dodeliti bar jedan sto!");
        }
        int ukupnoMestaI = Integer.valueOf(UkupnoMesta);
        if (ukupnoMestaI < brojMestaI) {
            throw new RuntimeException("Niste obezbedili dovoljno mesta za rezervaciju!");
        }
        
        Rezervacija rezervacija = new Rezervacija(sifraRezervacije, naIme, brojMestaI, vremeD, Napomena);
        rezervacija.setStolovi(stolovi);
        return rezervacija;
    }
    
    private void popuniFormu(Rezervacija r) {
        model = new StoTableModel();
        List<Sto> stolovi = r.getStolovi();
        for (Sto s : stolovi) {
            s.setStatus(Status.STARI);
        }
        model.setStolovi(r.getStolovi());
        jTable1.setModel(model);
        jTxtSifra.setText(sifraRezervacije + "");
        jTxtNaIme.setText(r.getNaIme());
        jTxtVreme.setText(sdf.format(r.getVreme()));
        jTxtBrojMesta.setText(r.getBrojMesta() + "");
        jTxtAreaNapomena.setText(r.getNapomena());
        jCBStolovi.removeAllItems();
    }
