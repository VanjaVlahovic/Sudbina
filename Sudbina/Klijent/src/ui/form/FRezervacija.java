    private void jBtnSacuvajActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jBtnSacuvajActionPerformed
        try {
            Rezervacija rezervacija = validacija();
            if(mode.equals("edit")){
                List<Sto> obrisani = model.getObrisani();
                List<Sto> sviStolovi = new ArrayList<>();
                sviStolovi.addAll(obrisani);
                sviStolovi.addAll(rezervacija.getStolovi());
                rezervacija.setStolovi(sviStolovi);
            }
            Controller.getInstance().sacuvajRezervaciju(rezervacija);
            JOptionPane.showMessageDialog(this, "Sistem je zapamtio rezervaciju!");
            this.dispose();
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_jBtnSacuvajActionPerformed

    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        
        Date vreme = new Date();
        jCBStolovi.removeAllItems();
        try {
            vreme = sdf.parse(jTxtVreme.getText());
        } catch (ParseException ex) {
            JOptionPane.showMessageDialog(this, "Vreme nije uneto u ispravnom formatu!");
        }
        try {
            List<Sto> stolovi = Controller.getInstance().vratiStolove(vreme);
            for (Sto sto : stolovi) {
                jCBStolovi.addItem(sto);
            }
        } catch (Exception ex) {
            JOptionPane.showMessageDialog(this, ex.getMessage());
        }
    }//GEN-LAST:event_jButton1ActionPerformed

