    private void validacija(JTextField jTxtKorisnickoIme, JPasswordField jTxtSifra) throws Exception {
        jLblPoruka.setText("");

        if (jTxtKorisnickoIme.getText().isEmpty()) {
            jLblPoruka.setText("Korisnicko ime je prazno!");
            throw new Exception("Korisnicko ime ne sme biti prazno");
        }

        if (String.valueOf(jTxtSifra.getPassword()).isEmpty()) {
            jLblPoruka.setText("Sifra je prazna!");
            throw new Exception("Sifra ne sme biti prazna");
        }
    }
